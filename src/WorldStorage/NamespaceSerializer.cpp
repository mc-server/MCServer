std::pair<NamespaceSerializer::Namespace, std::string_view> NamespaceSerializer::SplitNamespacedID(const std::string_view ID)
{
	const auto NamespaceIndex = ID.find(':');
	if (NamespaceIndex == std::string_view::npos)
	{
		// No explicit namespace defaults to the Minecraft namespace:
		return { Namespace::Minecraft, ID };
	}

	const auto Namespace = ID.substr(0, NamespaceIndex);
	if (Namespace == "minecraft")
	{
		// An unprefixed ID in the vanilla Minecraft namespace.
		const auto Value = ID.substr(NamespaceIndex + 1);

		return { Namespace::Minecraft, Value };
	}

	if (Namespace == "cuberite")
	{
		return { Namespace::Cuberite, ID };
	}

	return { Namespace::Unknown, ID };
}





AString NamespaceSerializer::DePrettify(std::string_view a_ID)
{
	if (a_ID.size() == 0)
	{
		return "";
	}
	AString Result;

	// Convert CamelCase to snake_case:
	for (size_t i = 0; i < a_ID.size(); ++i)
	{
		if (i > 0 && isupper(a_ID[i]))
		{
			Result.push_back('_');
		}
		Result.push_back(static_cast<char>(tolower(static_cast<int>(a_ID[i]))));  // Todo(12xx12): WHYYYYYYYY
	}
	return Result;
}





bool NamespaceSerializer::IsPretty(std::string_view a_ID)
{
	// Check if a_ID is in CamelCase with regex
	return std::regex_match(a_ID.begin(), a_ID.end(), std::regex("([A-Z][a-z1-9]+)+"));
}





AString NamespaceSerializer::Prettify(AString a_ID)
{

	bool NextLetterCapitalized = true;
	std::for_each(a_ID.begin(), a_ID.end(), [&](char & a_Letter)
	{
		if (NextLetterCapitalized)
		{
			a_Letter = static_cast<char>(std::toupper(a_Letter));
			NextLetterCapitalized = false;
		}
		else if (a_Letter == '_')
		{
			a_Letter = ' ';
			NextLetterCapitalized = true;
		}
	});
	return a_ID;
}





AString NamespaceSerializer::PrettifyEntityName(const AString & a_ID, const bool a_IsTamed)
{
	// In older vanilla Minecraft version (before 1.14) ocelots and cats were the same mob.
	// So after killing a tamed ocelot without a custom name the message will say "Cat was slain by [PlayerName]".
	if ((a_ID == "ocelot") && a_IsTamed)
	{
		return "Cat";
	}
	return Prettify(a_ID);
}
